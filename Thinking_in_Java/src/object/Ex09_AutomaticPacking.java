package object;

public class Ex09_AutomaticPacking {

	public static void main(String[] args) {
        Boolean boo = true;
        Character c = 'a';
        Byte by = (byte)12;
        Short sh = (short)20;
        Integer in = 100;
        Long lon = 2000L;
        Float fl = 1.6F;
        Double dou = 10.564d;
        System.out.println(boo + ", " + c + ", " + by + ", " + 
            sh + ", " + in + ", " + lon + ", " + fl + ", " + dou);
	}

}
