package object;

public class Ex09_AutomaticPacking {

	public static void main(String[] args) {
        Boolean bo = true;
        boolean boo = bo;
        Character ch = 'a';
        char c = ch;
        Byte bt = 1;
        byte by = bt;
        Short st = (short)20;
        short sh = st;
        Integer ie = 100;
        int in = ie;
        Long lo = 2000L;
        long lon = lo;
        Float ft = 1.6F;
        float fl = ft;
        Double db = 10.564d;
        double dou = db;
        System.out.println("boolean: " + boo + ", char: " + c + ", byte:" + by + ", short: " + 
            sh + ", int: " + in + ", long: " + lon + ", float: " + fl + ", double: " + dou);
	}

}
